class Practice09_01 {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            if (i == 4) {
                System.out.println(10);
            } else if (i == 5) {
                System.out.println(20);
            } else {
                System.out.println(i * 2);
            }
        }
    }
}
